import Foundation

public enum WsAgentDeviceCommand: String, Codable, Sendable {
    case status = "device.status"
    case info = "device.info"
}

public enum WsAgentBatteryState: String, Codable, Sendable {
    case unknown
    case unplugged
    case charging
    case full
}

public enum WsAgentThermalState: String, Codable, Sendable {
    case nominal
    case fair
    case serious
    case critical
}

public enum WsAgentNetworkPathStatus: String, Codable, Sendable {
    case satisfied
    case unsatisfied
    case requiresConnection
}

public enum WsAgentNetworkInterfaceType: String, Codable, Sendable {
    case wifi
    case cellular
    case wired
    case other
}

public struct WsAgentBatteryStatusPayload: Codable, Sendable, Equatable {
    public var level: Double?
    public var state: WsAgentBatteryState
    public var lowPowerModeEnabled: Bool

    public init(level: Double?, state: WsAgentBatteryState, lowPowerModeEnabled: Bool) {
        self.level = level
        self.state = state
        self.lowPowerModeEnabled = lowPowerModeEnabled
    }
}

public struct WsAgentThermalStatusPayload: Codable, Sendable, Equatable {
    public var state: WsAgentThermalState

    public init(state: WsAgentThermalState) {
        self.state = state
    }
}

public struct WsAgentStorageStatusPayload: Codable, Sendable, Equatable {
    public var totalBytes: Int64
    public var freeBytes: Int64
    public var usedBytes: Int64

    public init(totalBytes: Int64, freeBytes: Int64, usedBytes: Int64) {
        self.totalBytes = totalBytes
        self.freeBytes = freeBytes
        self.usedBytes = usedBytes
    }
}

public struct WsAgentNetworkStatusPayload: Codable, Sendable, Equatable {
    public var status: WsAgentNetworkPathStatus
    public var isExpensive: Bool
    public var isConstrained: Bool
    public var interfaces: [WsAgentNetworkInterfaceType]

    public init(
        status: WsAgentNetworkPathStatus,
        isExpensive: Bool,
        isConstrained: Bool,
        interfaces: [WsAgentNetworkInterfaceType])
    {
        self.status = status
        self.isExpensive = isExpensive
        self.isConstrained = isConstrained
        self.interfaces = interfaces
    }
}

public struct WsAgentDeviceStatusPayload: Codable, Sendable, Equatable {
    public var battery: WsAgentBatteryStatusPayload
    public var thermal: WsAgentThermalStatusPayload
    public var storage: WsAgentStorageStatusPayload
    public var network: WsAgentNetworkStatusPayload
    public var uptimeSeconds: Double

    public init(
        battery: WsAgentBatteryStatusPayload,
        thermal: WsAgentThermalStatusPayload,
        storage: WsAgentStorageStatusPayload,
        network: WsAgentNetworkStatusPayload,
        uptimeSeconds: Double)
    {
        self.battery = battery
        self.thermal = thermal
        self.storage = storage
        self.network = network
        self.uptimeSeconds = uptimeSeconds
    }
}

public struct WsAgentDeviceInfoPayload: Codable, Sendable, Equatable {
    public var deviceName: String
    public var modelIdentifier: String
    public var systemName: String
    public var systemVersion: String
    public var appVersion: String
    public var appBuild: String
    public var locale: String

    public init(
        deviceName: String,
        modelIdentifier: String,
        systemName: String,
        systemVersion: String,
        appVersion: String,
        appBuild: String,
        locale: String)
    {
        self.deviceName = deviceName
        self.modelIdentifier = modelIdentifier
        self.systemName = systemName
        self.systemVersion = systemVersion
        self.appVersion = appVersion
        self.appBuild = appBuild
        self.locale = locale
    }
}
